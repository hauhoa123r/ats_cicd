BEGIN;

-- =============================================================================
-- ATS PostgreSQL schema and test data
-- Source of truth: ats_be/src/main/java/org/ats/entities
--
-- Intentional application-level constraints beyond the current JPA annotations:
--   * users.email is UNIQUE because authentication loads one Optional<User> by email.
--   * job salaries are non-negative and salary_max cannot be lower than salary_min.
--   * nullable parent references use ON DELETE SET NULL; owned token/join rows use
--     ON DELETE CASCADE. JPA does not declare database-level @OnDelete behavior.
-- Job.status remains VARCHAR because the entity does not use @Enumerated; seeded values
-- nevertheless follow JobStatus.java: DRAFT, ACTIVE, INACTIVE.
-- =============================================================================

-- =============================================================================
-- DROP TABLES
-- Join tables first, then child tables, then parent tables.
-- =============================================================================

DROP TABLE IF EXISTS user_roles CASCADE;
DROP TABLE IF EXISTS job_skills CASCADE;
DROP TABLE IF EXISTS refresh_tokens CASCADE;
DROP TABLE IF EXISTS jobs CASCADE;
DROP TABLE IF EXISTS users CASCADE;
DROP TABLE IF EXISTS skills CASCADE;
DROP TABLE IF EXISTS roles CASCADE;
DROP TABLE IF EXISTS departments CASCADE;

-- =============================================================================
-- CREATE TABLES
-- =============================================================================

CREATE TABLE departments (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY,
    department_name VARCHAR(255) NOT NULL,
    description     TEXT,
    created_at      TIMESTAMP WITHOUT TIME ZONE,
    created_by      VARCHAR(255),
    deleted_at      TIMESTAMP WITHOUT TIME ZONE,
    is_deleted      BOOLEAN,
    updated_at      TIMESTAMP WITHOUT TIME ZONE,
    updated_by      VARCHAR(255),
    CONSTRAINT pk_departments PRIMARY KEY (id),
    CONSTRAINT uq_departments_department_name UNIQUE (department_name)
);

CREATE TABLE roles (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY,
    name        VARCHAR(255) NOT NULL,
    description VARCHAR(255),
    CONSTRAINT pk_roles PRIMARY KEY (id),
    CONSTRAINT uq_roles_name UNIQUE (name)
);

CREATE TABLE skills (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY,
    skill_name  VARCHAR(255),
    category    VARCHAR(255),
    created_at  TIMESTAMP WITHOUT TIME ZONE,
    created_by  VARCHAR(255),
    deleted_at  TIMESTAMP WITHOUT TIME ZONE,
    is_deleted  BOOLEAN,
    updated_at  TIMESTAMP WITHOUT TIME ZONE,
    updated_by  VARCHAR(255),
    CONSTRAINT pk_skills PRIMARY KEY (id)
);

CREATE TABLE users (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY,
    full_name       VARCHAR(255) NOT NULL,
    email           VARCHAR(255) NOT NULL,
    password_hash   VARCHAR(255) NOT NULL,
    phone           VARCHAR(30),
    role            VARCHAR(50),
    sso_provider_id VARCHAR(255),
    status          VARCHAR(50),
    department_id   BIGINT,
    created_at      TIMESTAMP WITHOUT TIME ZONE,
    created_by      VARCHAR(255),
    deleted_at      TIMESTAMP WITHOUT TIME ZONE,
    is_deleted      BOOLEAN,
    updated_at      TIMESTAMP WITHOUT TIME ZONE,
    updated_by      VARCHAR(255),
    CONSTRAINT pk_users PRIMARY KEY (id),
    CONSTRAINT uq_users_email UNIQUE (email),
    CONSTRAINT fk_users_department
        FOREIGN KEY (department_id)
        REFERENCES departments (id)
        ON DELETE SET NULL
);

CREATE TABLE jobs (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY,
    title           VARCHAR(255),
    description     TEXT,
    location        VARCHAR(255),
    salary_min      NUMERIC(15, 2),
    salary_max      NUMERIC(15, 2),
    status          VARCHAR(255),
    utm_source      VARCHAR(255),
    utm_medium      VARCHAR(255),
    deadline        TIMESTAMP WITHOUT TIME ZONE,
    published_at    TIMESTAMP WITHOUT TIME ZONE,
    department_id   BIGINT,
    recruiter_id    BIGINT,
    employment_type VARCHAR(255),
    created_at      TIMESTAMP WITHOUT TIME ZONE,
    created_by      VARCHAR(255),
    deleted_at      TIMESTAMP WITHOUT TIME ZONE,
    is_deleted      BOOLEAN,
    updated_at      TIMESTAMP WITHOUT TIME ZONE,
    updated_by      VARCHAR(255),
    CONSTRAINT pk_jobs PRIMARY KEY (id),
    CONSTRAINT chk_jobs_salary_min_nonnegative
        CHECK (salary_min IS NULL OR salary_min >= 0),
    CONSTRAINT chk_jobs_salary_max_nonnegative
        CHECK (salary_max IS NULL OR salary_max >= 0),
    CONSTRAINT chk_jobs_salary_range
        CHECK (salary_min IS NULL OR salary_max IS NULL OR salary_max >= salary_min),
    CONSTRAINT fk_jobs_department
        FOREIGN KEY (department_id)
        REFERENCES departments (id)
        ON DELETE SET NULL,
    CONSTRAINT fk_jobs_recruiter
        FOREIGN KEY (recruiter_id)
        REFERENCES users (id)
        ON DELETE SET NULL
);

CREATE TABLE refresh_tokens (
    id         BIGINT GENERATED BY DEFAULT AS IDENTITY,
    token_hash VARCHAR(100) NOT NULL,
    user_id    BIGINT NOT NULL,
    expires_at TIMESTAMP WITH TIME ZONE NOT NULL,
    revoked    BOOLEAN NOT NULL,
    CONSTRAINT pk_refresh_tokens PRIMARY KEY (id),
    CONSTRAINT uq_refresh_tokens_token_hash UNIQUE (token_hash),
    CONSTRAINT fk_refresh_tokens_user
        FOREIGN KEY (user_id)
        REFERENCES users (id)
        ON DELETE CASCADE
);

CREATE TABLE job_skills (
    job_id   BIGINT NOT NULL,
    skill_id BIGINT NOT NULL,
    CONSTRAINT pk_job_skills PRIMARY KEY (job_id, skill_id),
    CONSTRAINT fk_job_skills_job
        FOREIGN KEY (job_id)
        REFERENCES jobs (id)
        ON DELETE CASCADE,
    CONSTRAINT fk_job_skills_skill
        FOREIGN KEY (skill_id)
        REFERENCES skills (id)
        ON DELETE CASCADE
);

CREATE TABLE user_roles (
    user_id BIGINT NOT NULL,
    role_id BIGINT NOT NULL,
    CONSTRAINT pk_user_roles PRIMARY KEY (user_id, role_id),
    CONSTRAINT fk_user_roles_user
        FOREIGN KEY (user_id)
        REFERENCES users (id)
        ON DELETE CASCADE,
    CONSTRAINT fk_user_roles_role
        FOREIGN KEY (role_id)
        REFERENCES roles (id)
        ON DELETE CASCADE
);

-- =============================================================================
-- CREATE INDEXES
-- PostgreSQL does not automatically index foreign-key columns. Composite primary
-- keys already cover job_skills.job_id and user_roles.user_id.
-- =============================================================================

CREATE INDEX idx_users_department_id ON users (department_id);
CREATE INDEX idx_jobs_department_id ON jobs (department_id);
CREATE INDEX idx_jobs_recruiter_id ON jobs (recruiter_id);
CREATE INDEX idx_refresh_tokens_user_id ON refresh_tokens (user_id);
CREATE INDEX idx_job_skills_skill_id ON job_skills (skill_id);
CREATE INDEX idx_user_roles_role_id ON user_roles (role_id);

-- Required explicitly by RefreshToken's @Table(indexes = ...). This is redundant
-- with the UNIQUE index on token_hash, but is retained for annotation fidelity.
CREATE INDEX idx_refresh_token_hash ON refresh_tokens (token_hash);

